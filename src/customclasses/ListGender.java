package customclasses;

public class ListGender {
	
	private int id;
	private String gender;
	
	public ListGender(int id, String gender) {
		this.id = id;
		this.gender = gender;
	}
	
	public int getID() {
		return this.id;
	}
	
	public String getGender() {
		return this.gender;
	}

}
